    private void scheduleCourseSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseSubmitButtonActionPerformed
        String courseCode = selectCourseComboBox.getSelectedItem().toString();
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        CourseEntry course = null;
        
        for (ScheduleEntry entry : ScheduleQueries.getScheduleByStudent(currentSemester, currentStudent.getStudentID())) {
            if (entry.getCourseCode().equals(courseCode)) {
                scheduleCourseStatusLabel.setText("Student is already enrolled.");
                return;
            }
        }
        
        for (CourseEntry c: courses){
            if (c.getCourseCode().equals(courseCode)){
                course = c;
            }
        }      
        Timestamp timestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());

        ScheduleEntry sch;
        int seatsTaken = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        if(seatsTaken < course.getSeats()){
            sch = new ScheduleEntry(currentSemester, courseCode, currentStudent.getStudentID(), "Scheduled", timestamp);
            scheduleCourseStatusLabel.setText(currentStudent + " has been Scheduled for " + courseCode);
        } else {
            sch = new ScheduleEntry(currentSemester, courseCode, currentStudent.getStudentID(), "Waitlisted", timestamp);
            scheduleCourseStatusLabel.setText(currentStudent + " has been Waitlisted for " + courseCode);
        }
        ScheduleQueries.addScheduleEntry(sch);
    }//GEN-LAST:event_scheduleCourseSubmitButtonActionPerformed

