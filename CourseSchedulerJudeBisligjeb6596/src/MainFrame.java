    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }

    //rebuilds all other combo boxes
    public void rebuildOtherComboBoxes()
    {
        ArrayList<String> codes = CourseQueries.getAllCourseCodes(currentSemester);
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        selectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(codes.toArray()));
        changeStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        displaySelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(codes.toArray()));
        dropSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(codes.toArray()));
        dropStudentAdminComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        dropCourseAdminComboBox.setModel(new javax.swing.DefaultComboBoxModel(codes.toArray()));
        
        if (students.size() > 0)
        {
            currentStudentLabel.setText(students.get(0) + "");
            currentStudent = students.get(0);
        }
        else
        {
            currentStudentLabel.setText("None, add a student.");
        }
    }
    
    private ScheduleEntry checkWaitlist(String semester, String courseCode){
        ScheduleEntry sch = null;
        ArrayList<ScheduleEntry> schedules = ScheduleQueries.getWaitlistedStudentsByCourse(semester, courseCode);
        if(!schedules.isEmpty()){
            sch = schedules.get(0);  
        }
        return sch;
    }
