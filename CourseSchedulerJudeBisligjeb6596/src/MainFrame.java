    private void dropStudentAdminButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentAdminButtonActionPerformed
        dropStudentAdminTextArea.setText(dropStudentAdminComboBox.getSelectedItem().toString() + " has been dropped from the list of students.");
        StudentEntry student = (StudentEntry) dropStudentAdminComboBox.getSelectedItem();
        StudentQueries.dropStudent(student.getStudentID());
        ArrayList<String> semesters = SemesterQueries.getSemesterList();

        for (String semester: semesters){
            
            dropStudentAdminTextArea.append("\n\nFor Semester: " + semester);
            ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semester, student.getStudentID());
            
            for (ScheduleEntry schedule: schedules){
                if(schedule.getStatus().equals("Scheduled")){
                    ScheduleQueries.dropStudentScheduleByCourse(semester, student.getStudentID(), schedule.getCourseCode());
                    dropStudentAdminTextArea.append("\n" + dropStudentAdminComboBox.getSelectedItem().toString() + " has been dropped from " + schedule.getCourseCode());
                    ScheduleEntry offWaitlist = checkWaitlist(semester, schedule.getCourseCode());
                    
                    if (offWaitlist != null){
                        StudentEntry studentOff = StudentQueries.getStudent(offWaitlist.getStudentID());
                        dropStudentAdminTextArea.append("\n" + studentOff.getLastName() + ", " + studentOff.getFirstName() +  " has been scheduled into " + schedule.getCourseCode());
                        ScheduleQueries.updateScheduleEntry(semester, offWaitlist);
                    }
                } else {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, student.getStudentID(), schedule.getCourseCode());
                    dropStudentAdminTextArea.append("\n" + dropStudentAdminComboBox.getSelectedItem().toString() + " has been dropped from the waitlist for " + schedule.getCourseCode());
                }
            }
        }
        rebuildOtherComboBoxes();
    }//GEN-LAST:event_dropStudentAdminButtonActionPerformed

